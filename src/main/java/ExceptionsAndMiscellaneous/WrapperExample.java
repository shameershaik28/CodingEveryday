package ExceptionsAndMiscellaneous;

public class WrapperExample {
    public static void main(String[] args) {
        Integer myInt = 42;     // Autoboxing
        Double myDouble = 3.14; // Wrapper for double
        Boolean isJava = true;

        System.out.println(myInt + ", " + myDouble + ", " + isJava);
    }
}

