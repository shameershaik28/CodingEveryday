package ExceptionsAndMiscellaneous;

public class ObjectExample {
    public static void main(String[] args) {
        String name = "Java";

        System.out.println(name.toString());  // Output: Java
        System.out.println(name.equals("Java")); // Output: true
        System.out.println(name.getClass());  // Output: class java.lang.String
    }
}

